"use client";
import { motion } from "framer-motion";
import { ReactNode } from "react";

/**
 * Scroll-reveal wrapper: fades + slides its children up when they
 * enter the viewport. Used to give every homepage section a smooth
 * appearance on scroll.
 */
export default function Reveal({
  children,
  delay = 0,
  className = "",
}: {
  children: ReactNode;
  delay?: number;
  className?: string;
}) {
  return (
    <motion.div
      initial={{ opacity: 0, y: 40 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, amount: 0.2 }}
      transition={{ duration: 0.6, delay, ease: "easeOut" }}
      className={className}
    >
      {children}
    </motion.div>
  );
}
